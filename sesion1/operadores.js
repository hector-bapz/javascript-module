
let numero1 =10
let numero2 = 2

//sumar
console.log(numero1 + numero2);

//restar
console.log(numero1 - numero2);

//multiplicación
console.log(numero1 * numero2);

//división
console.log(numero1 / numero2);

//exponenciación
console.log(numero1 ** numero2);

//módulo, residuo o resto
console.log(numero1 % numero2);

//incremental (se incrementa 1)
console.log(++numero1);

//decremental (se decrementa por 1)
console.log(--numero1);

//== comparación entre 2 valores
//=== estrictamente igual. no solo compara valores, si no que también el tipo de dato de la varibale
